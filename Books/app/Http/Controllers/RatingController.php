     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $rating = Rating::where('id_author', $request->get('author'))->where('id_book', $request->get('book'))->first();

        $rating->rating = $rating->rating + $request->get('rating');
        $rating->voter = $rating->voter + 1;

        $rating->save();
        $rating->average = $rating->rating / $rating->voter;
        $rating->save();
        return redirect('/');
    }

